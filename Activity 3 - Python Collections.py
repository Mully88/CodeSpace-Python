# Python program to find the maximum and minimum value of a list

# Define the sample list
numbers = [25, 14, 56, 15, 36, 56, 77, 18, 29, 49]

# Print the original list
print("Original List:", numbers)

# Use the built-in max() function to find the maximum value
maximum_value = max(numbers)

# Use the built-in min() function to find the minimum value
minimum_value = min(numbers)

# Print the results
print("Maximum value for the above list =", maximum_value)
print("Minimum value for the above list =", minimum_value)
